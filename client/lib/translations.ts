// Translation hook for React components
export function useTranslation(language: "ar" | "en" = "en") {
  const t = (key: keyof TranslationStrings): string => {
    return translations[language][key] || translations.en[key] || key;
  };

  return { t, language };
}

// Set document direction based on language
export function setDocumentDirection(language: "ar" | "en") {
  const direction = language === "ar" ? "rtl" : "ltr";
  document.documentElement.setAttribute("dir", direction);
  document.documentElement.setAttribute("lang", language);
}

// Format numbers for different locales
export function formatNumber(num: number, language: "ar" | "en"): string {
  const locale = language === "ar" ? "ar-SA" : "en-US";
  return new Intl.NumberFormat(locale).format(num);
}

// Format time for different locales
export function formatTime(seconds: number, language: "ar" | "en"): string {
  const mins = Math.floor(seconds / 60);
  const secs = Math.floor(seconds % 60);
  const formattedMins = mins.toString().padStart(2, "0");
  const formattedSecs = secs.toString().padStart(2, "0");

  if (language === "ar") {
    return `${formattedSecs}:${formattedMins}`;
  }
  return `${formattedMins}:${formattedSecs}`;
}

// Get appropriate font family for language
export function getFontFamily(language: "ar" | "en"): string {
  if (language === "ar") {
    return '"Cairo", "Noto Sans Arabic", "Segoe UI", sans-serif';
  }
  return '"SF Pro Display", "Segoe UI", "Roboto", sans-serif';
}
